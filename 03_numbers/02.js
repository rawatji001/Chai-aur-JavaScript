//! to string()
//* To convert a number to a string in JavaScript, you can use a few different methods:

//first ----------------
const num = 400;

console.log(num);
console.log(typeof num);

//* first----------------
console.log(num.toString());
console.log(typeof num.toString());

//* second----------------
console.log(String(num));
console.log(typeof String(num));

//* third----------------
console.log(num + "");
console.log(typeof (num + ""));
