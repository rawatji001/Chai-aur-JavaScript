const animals = ["ant", "bison", "camel", "bison", "duck", "elephant"];
// -------------------------------------------

//! indexOf(): Finds the index of the first occurrence of an element in an array.
console.log(animals.indexOf("bison")); //* 1

console.log(animals.indexOf("bison")); //* 1

// -------------------------------------------

//! includes(): Checks if an array contains a specific element and returns a boolean.
console.log(animals.includes("bison")); //* true

console.log(animals.includes("roshan")); //* false
// -------------------------------------------

//! to find element with index value-----------
console.log(animals[3]); //* duck

console.log(animals[9]); //* undefined

console.log(animals[-2]); //* undefined
